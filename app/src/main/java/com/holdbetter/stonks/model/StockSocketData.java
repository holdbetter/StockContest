package com.holdbetter.stonks.model;

public class StockSocketData {
    private String s;
    private double p;
    private long t;

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }

    public long getT() {
        return t;
    }

    public void setT(long t) {
        this.t = t;
    }
}

